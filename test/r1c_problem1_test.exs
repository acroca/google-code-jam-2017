defmodule GcjR1CTest1 do
  @p Gcj.R1C.Problem1

  use ExUnit.Case
  doctest Gcj.R1C.Problem1
  import Gcj.TestMacros

  test "parsing input" do
    input = [
      "4",
      "2 1",
      "100 20",
      "200 10",
      "2 2",
      "100 20",
      "200 10",
      "3 2",
      "100 10",
      "100 10",
      "100 10",
      "4 2",
      "9 3",
      "7 1",
      "10 1",
      "8 4"
    ]
    assert Enum.to_list(@p.parseInput(input)) == [
      {2,1,[{100,20},{200,10}]},
      {2,2,[{100,20},{200,10}]},
      {3,2,[{100,10},{100,10},{100,10}]},
      {4,2,[{9,3},{7,1},{10,1},{8,4}]}
    ]
  end

  t @p, {2,1,[{100,20},{200,10}]}, 138230.076758
  t @p, {2,2,[{100,20},{200,10}]}, 150796.4473723
  t @p, {3,2,[{100,10},{100,10},{100,10}]}, 43982.2971503
  t @p, {4,2,[{9,3},{7,1},{10,1},{8,4}]}, 625.1769381
end
